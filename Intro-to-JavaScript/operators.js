/**
 * types of operators in JavaScript. 
 * arithmetic operators => 
 *    1. arithmetic or concat operator ( + ) for addition or concatenation
 *    2. subtraction operator ( - ) for subtraction or minus
 *    3. multiplication operator ( * ) for multiply
 *    4. division operator ( / ) for divide
 *    5. modulus operator ( % ) for reminder 
 *    6. exponentiation operator ( ** ) for returns the result of raising the first operand to the power of the second operand. 
 *          It is equivalent to Math.pow(), except it also accepts BigInts as operands.
 *    7. increment ( ++ ) for increasing the value 
 *    8. decrement ( -- ) for decreasing the value
 * 
 * assignment operators => 
 *    1. assignment operator ( = ) for assigning value
 *    2. add and assignment operator ( += ) for add and assigning value into a variable
 *    3. subtract and assignment operator ( -= ) for subtract and assigning value into a variable
 *    4. multiply and assignment operator ( *= ) for multiply and assigning value into a variable
 *    5. divide and assignment operator ( /= ) for divide and assigning value into a variable
 *    6. modulus or reminder and assignment operator ( %= ) for modulus or reminding value assigning into a variable
 *    7. exponentiation and assignment operator ( **= ) for exponentiation or power value assigning into a variable
 *    8. logical and assignment operator ( &&= )
 *    9. logical or assignment operator ( ||= )
 *    10. nullish coalescing assignment operator ( ??= )
 *    11. spread operator ( ... )
 * 
 * logical operator => 
 *    1. logical and ( && ) for a set of boolean operands will be true if and only if all the operands are true. Otherwise it will be false.
 *    2. logical or ( || )
 *    3. logical not ( ! )
 * 
 * comparison operators => 
 *    1. equal to ( == ) for equal values
 *    2. equal value and type ( === ) for equal value and equal type
 *    3. not equal ( != )
 *    4. not equal value or type ( !== )
 *    5. greater than ( > )
 *    6. less than ( < )
 *    7. greater than or equal to ( >= )
 *    8. less than or equal to ( <= )
 */
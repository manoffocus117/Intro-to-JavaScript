/** Array in javascript
 * array is a collection of data items.
 * array is a ordered list of data items.
 * array is a mutable data type. (mutable means we can change the value of 
 * the array items.)
 * enables storing multiple values in a single variable.
 * array is a zero-based index. (zero-based index means the first item is at index 0)
 * 
 */

let numbers = [1, 22, 43, 67, 69,];
console.log(numbers);

let fruits = ["apple", "banana", "orange", "mango", "pineapple"];
console.log(fruits);

let friends = ["abul", "babul", "chabul", "kabul", "hablu"];
console.log(friends);